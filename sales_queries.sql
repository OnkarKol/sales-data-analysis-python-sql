CREATE DATABASE sales_analysis_project;

#USE → tells MySQL that all further queries should run inside this database.
USE sales_analysis_project;

#Create a table to store dataset.
CREATE TABLE sales(
Order_id VARCHAR(50),
Order_date DATE,
Customer_name VARCHAR(100),
region VARCHAR(50),
category VARCHAR(50),
sales FLOAT,
profit FLOAT 
);

