# Query 1: Total Sales.
SELECT SUM(sales) AS total_sales
FROM cleaned_superstore; # SUM(Sales) = adds all sales values.And it will give u total comapny revenue.

# Query 2 — Total Profit.
SELECT SUM(profit) AS total_profit
FROM cleaned_superstore; #Calculates overall profit generated by the company.

# Query 3 — Sales by Region.
SELECT region,SUM(sales) AS total_sales
FROM cleaned_superstore
GROUP BY region; #GROUP BY Region Groups sales data for each region.

# Query 4 — Top 5 Customers.
SELECT `Customer name`,SUM(sales) AS Total_sales
FROM cleaned_superstore
GROUP BY `Customer name`
ORDER BY total_sales DESC 
LIMIT 5;
