# Query 5 — Sales by Category.
SELECT category, SUM(sales) AS Total_sales
FROM cleaned_superstore
GROUP BY category; #Groups revenue by product category.

# Query 6 — Monthly Sales Trend.

ALTER TABLE cleaned_superstore
MODIFY `Order Date` DATE;

SELECT MONTH(`Order Date`) AS month,
SUM(Sales) AS total_sales
FROM cleaned_superstore
GROUP BY MONTH(`Order Date`)
ORDER BY MONTH(`Order Date`);
#MONTH(\Order Date`)` correctly extracted months 1–12
#SUM(Sales) correctly calculated total sales per month
#GROUP BY MONTH(\Order Date`)` grouped the data properly.

# Query 7 — Top 5 Products by Sales.
SELECT `Product Name`,
SUM(Sales) AS total_sales
FROM cleaned_superstore
GROUP BY `Product Name`
ORDER BY total_sales DESC
LIMIT 5;
# Groups sales by product
# Sorts products from highest to lowest sales
# Shows top 5 best-selling products

# Query 8 — Sales by Segment.

SELECT segment, SUM(sales) AS total_sales 
FROM cleaned_superstore
GROUP BY segment; # This shows which customer segment generates the most revenue.

# Query 9 — Average Sales per Order.
SELECT AVG(sales) AS avg_sales
FROM cleaned_superstore; # AVG() calculates the average value of orders.

# Query 10 — Profit by Region.
SELECT region, SUM(profit) AS total_profit
FROM cleaned_superstore
GROUP BY region
ORDER BY total_profit DESC; # Shows which region generates the highest profit, not just sales.

